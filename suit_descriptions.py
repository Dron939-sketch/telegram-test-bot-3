def get_suit_by_axes(control: float, internal: float, scores: dict = None) -> str:
    """
    –û–ø—Ä–µ–¥–µ–ª—è–µ—Ç –º–∞—Å—Ç—å —Å —É—á—ë—Ç–æ–º –ø—Ä–æ—Ü–µ–Ω—Ç–Ω–æ–≥–æ —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è –±–∞–ª–ª–æ–≤
    
    Args:
        control: –†–∞–∑–Ω–∏—Ü–∞ –º–µ–∂–¥—É –≤–Ω–µ—à–Ω–∏–º –∏ –≤–Ω—É—Ç—Ä–µ–Ω–Ω–∏–º —Ñ–æ–∫—É—Å–æ–º
        internal: –†–∞–∑–Ω–∏—Ü–∞ –º–µ–∂–¥—É —Å—Ç—Ä–∞—Ö–æ–º —É–º–æ–∑—Ä–∏—Ç–µ–ª—å–Ω–æ–≥–æ –∏ —Ñ–∞–∫—Ç–∏—á–µ—Å–∫–æ–≥–æ
        scores: –°–ª–æ–≤–∞—Ä—å —Å –∏—Å—Ö–æ–¥–Ω—ã–º–∏ –±–∞–ª–ª–∞–º–∏ –¥–ª—è –¥–µ—Ç–∞–ª—å–Ω–æ–≥–æ –∞–Ω–∞–ª–∏–∑–∞
    
    Returns:
        –ú–∞—Å—Ç—å: "clubs", "hearts", "diamonds", "spades"
    """
    
    # –ë–∞–∑–æ–≤–æ–µ –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ –ø–æ –∫–≤–∞–¥—Ä–∞–Ω—Ç–∞–º
    base_suit = None
    if control >= 0 and internal >= 0:
        base_suit = "clubs"
    elif control < 0 and internal >= 0:
        base_suit = "hearts"
    elif control < 0 and internal < 0:
        base_suit = "spades"
    else:
        base_suit = "diamonds"
    
    # –ï—Å–ª–∏ –µ—Å—Ç—å –¥–µ—Ç–∞–ª—å–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ, –ø—Ä–æ–≤–µ—Ä—è–µ–º –≥—Ä–∞–Ω–∏—á–Ω—ã–µ —Å–ª—É—á–∞–∏
    if scores:
        total = sum(scores.values())
        if total > 0:
            # –í—ã—á–∏—Å–ª—è–µ–º –ø—Ä–æ—Ü–µ–Ω—Ç–Ω–æ–µ —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ
            percentages = {k: (v / total) * 100 for k, v in scores.items()}
            
            # –ï—Å–ª–∏ –∫–∞–∫–∞—è-—Ç–æ –æ—Å—å –¥–æ–º–∏–Ω–∏—Ä—É–µ—Ç –±–æ–ª–µ–µ —á–µ–º –Ω–∞ 60%, —ç—Ç–æ –≤–ª–∏—è–µ—Ç –Ω–∞ —Ä–µ–∑—É–ª—å—Ç–∞—Ç
            max_axis = max(percentages, key=percentages.get)
            max_percent = percentages[max_axis]
            
            if max_percent > 60:
                # –°–∏–ª—å–Ω–∞—è –¥–æ–º–∏–Ω–∞–Ω—Ç–∞ –æ–¥–Ω–æ–π –æ—Å–∏
                if max_axis == "VOVNE":
                    return "clubs" if internal >= 0 else "diamonds"
                elif max_axis == "VNUTR":
                    return "hearts" if internal >= 0 else "spades"
                elif max_axis == "UMOZ":
                    return "clubs" if control >= 0 else "hearts"
                elif max_axis == "FAKT":
                    return "diamonds" if control >= 0 else "spades"
    
    return base_suit


