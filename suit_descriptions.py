    balance = analyze_suit_balance(scores)
    
    return {
        "suit": suit,
        "suit_name": get_suit_name(suit),
        "scores": scores,
        "percentages": percentages,
        "axes": {
            "control": control,
            "internal": internal
        },
        "balance": balance,
        "analytics": SUIT_ANALYTICS_INTERNAL.get(suit, {})
    }
