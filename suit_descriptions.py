def analyze_suit_balance(scores: dict) -> dict:
    """
    –ê–Ω–∞–ª–∏–∑–∏—Ä—É–µ—Ç –±–∞–ª–∞–Ω—Å –º–µ–∂–¥—É –æ—Å—è–º–∏ (–¥–ª—è –≤–Ω—É—Ç—Ä–µ–Ω–Ω–µ–≥–æ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è)
    
    Returns:
        dict —Å –∫–ª—é—á–∞–º–∏:
        - 'balance_type': 'balanced', 'control_dominant', 'fear_dominant', 'extreme'
        - 'dominant_axis': –Ω–∞–∑–≤–∞–Ω–∏–µ –¥–æ–º–∏–Ω–∏—Ä—É—é—â–µ–π –æ—Å–∏
        - 'balance_score': —á–∏—Å–ª–æ –æ—Ç 0 –¥–æ 100 (100 = –∏–¥–µ–∞–ª—å–Ω—ã–π –±–∞–ª–∞–Ω—Å)
    """
    total = sum(scores.values())
    if total == 0:
        return {
            'balance_type': 'balanced',
            'dominant_axis': None,
            'balance_score': 100,
            'max_percent': 25.0
        }
    
    percentages = {k: (v / total) * 100 for k, v in scores.items()}
    max_axis = max(percentages, key=percentages.get)
    max_percent = percentages[max_axis]
    
    # –í—ã—á–∏—Å–ª—è–µ–º –±–∞–ª–∞–Ω—Å (—á–µ–º –±–ª–∏–∂–µ –∫ 25% –∫–∞–∂–¥–∞—è –æ—Å—å, —Ç–µ–º –≤—ã—à–µ –±–∞–ª–∞–Ω—Å)
    ideal = 25.0
    deviations = [abs(p - ideal) for p in percentages.values()]
    avg_deviation = sum(deviations) / len(deviations)
    balance_score = max(0, 100 - (avg_deviation * 4))
    
    # –û–ø—Ä–µ–¥–µ–ª—è–µ–º —Ç–∏–ø –±–∞–ª–∞–Ω—Å–∞
    if max_percent > 70:
        balance_type = 'extreme'
    elif max_percent > 50:
        if max_axis in ['VOVNE', 'VNUTR']:
            balance_type = 'control_dominant'
        else:
            balance_type = 'fear_dominant'
    else:
        balance_type = 'balanced'
    
    return {
        'balance_type': balance_type,
        'dominant_axis': max_axis,
        'balance_score': round(balance_score, 1),
        'max_percent': round(max_percent, 1)
    }


