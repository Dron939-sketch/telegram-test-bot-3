def main():
    """Запуск бота"""
    application = Application.builder().token(TELEGRAM_BOT_TOKEN).build()
    
    conv_handler = ConversationHandler(
        entry_points=[
            CommandHandler("start", start),
            CallbackQueryHandler(start_test, pattern="^start_test$")
        ],
        states={
            STAGE_1: [
                CallbackQueryHandler(start_stage_1, pattern="^start_stage_1$"),
                CallbackQueryHandler(handle_stage_1_answer, pattern="^s1_")
            ],
            STAGE_2: [
                CallbackQueryHandler(start_stage_2, pattern="^start_stage_2$"),
                CallbackQueryHandler(handle_stage_2_answer, pattern="^s2_")
            ],
            STAGE_3: [
                CallbackQueryHandler(start_stage_3, pattern="^start_stage_3$"),
                CallbackQueryHandler(handle_stage_3_answer, pattern="^s3_")
            ]
        },
        fallbacks=[CommandHandler("cancel", cancel)]
    )
    
    application.add_handler(conv_handler)
    application.add_handler(CallbackQueryHandler(show_details, pattern="^show_details$"))
    application.add_handler(CallbackQueryHandler(back_to_start, pattern="^back_to_start$"))
    application.add_handler(CallbackQueryHandler(show_analytics, pattern="^show_analytics$"))
    application.add_handler(CallbackQueryHandler(back_to_stage1_result, pattern="^back_to_stage1_result$"))
    application.add_handler(CallbackQueryHandler(show_offer, pattern="^show_offer$"))
    application.add_handler(CallbackQueryHandler(show_details_offer, pattern="^show_details_offer$"))
    application.add_handler(CallbackQueryHandler(read_tale, pattern="^read_tale$"))
    application.add_handler(CallbackQueryHandler(tale_part2, pattern="^tale_part2$"))
    application.add_handler(CallbackQueryHandler(tale_part3, pattern="^tale_part3$"))
    application.add_handler(CallbackQueryHandler(tale_final, pattern="^tale_final$"))
    application.add_handler(CallbackQueryHandler(order_tale, pattern="^order_tale$"))
    application.add_handler(CallbackQueryHandler(back_to_result, pattern="^back_to_result$"))
    
    logger.info("✅ Бот запущен!")
    application.run_polling()

if __name__ == "__main__":
    main()
