def calculate_stage_3_result(scores):
    """
    Подсчитать результат Этапа 3 (определение проблемного уровня Дилтса)
    
    Args:
        scores: dict {"environment": 4, "behavior": 2, "capabilities": 0, ...}
    
    Returns:
        str: Уровень Дилтса ("environment", "behavior", "capabilities", "values", "identity", "mission", "none")
    """
    if not scores or not isinstance(scores, dict):
        return "environment"
    
    if all(v == 0 for v in scores.values()):
        return "none"
    
    max_level = max(scores, key=scores.get)
    max_score = scores[max_level]
    
    if max_score == 0:
        return "none"
    
    return max_level


