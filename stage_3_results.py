def format_stage3_result(suit, level):
    """
    Форматирует результат Этапа 3
    
    Args:
        suit: str ("clubs", "hearts", "diamonds", "spades")
        level: str ("environment", "behavior", "capabilities", "values", "identity", "mission", "none")
    
    Returns:
        str: Отформатированный текст результата
    """
    if suit not in STAGE_3_RESULTS:
        return "❌ Ошибка: область не найдена"
    
    if level not in STAGE_3_RESULTS[suit]:
        return "❌ Ошибка: уровень не найден"
    
    result = STAGE_3_RESULTS[suit][level]
    
    text_parts = [
        f"<b>{result['title']}</b>",
        f"<i>{result['subtitle']}</i>",
        ""
    ]
    
    text_parts.extend(result["text"])
    
    return "\n".join(text_parts)
