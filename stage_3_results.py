def format_stage3_result(suit, level):
    """Форматировать результат Этапа 3"""
    desc = get_stage3_description(suit, level)
    
    text_parts = []
    
    # Заголовок
    text_parts.append(f"🎯 <b>РЕЗУЛЬТАТ ЭТАПА 3</b>")
    text_parts.append("")
    text_parts.append(f"<b>{desc['title']}</b>")
    text_parts.append("")
    text_parts.append(f"<i>{desc['subtitle']}</i>")
    text_parts.append("")
    text_parts.append("━━━━━━━━━━━━━━━━━━━━")
    text_parts.append("")
    
    # Основной текст
    text_parts.extend(desc['text'])
    
    return "\n".join(text_parts)


def calculate_stage_3_result(answers):
    """
    Подсчитать результат Этапа 3
    Возвращает уровень с максимальным количеством проблем
    """
    level_counts = {
        "environment": 0,
        "behavior": 0,
        "capabilities": 0,
        "values": 0,
        "identity": 0,
        "mission": 0,
        "none": 0
    }
    
    for answer in answers:
        level = answer["level"]
        level_counts[level] += 1
    
    # Находим уровень с максимальным количеством проблем
    # Исключаем "none" из подсчета
    problem_levels = {k: v for k, v in level_counts.items() if k != "none"}
    
    if not problem_levels or max(problem_levels.values()) == 0:
        return "none"  # Нет проблем вообще
    
    max_level = max(problem_levels, key=problem_levels.get)
    return max_level


